package application.model.ArbitroPackage;

/**
 * A classe <b> Arbitro </b> serve para criar um objeto que será um cadastro de um Árbitro da copa do mundo no sistema.
 * @author Mailson
 * @since 2022
 */
public class Arbitro {
	/**
	 * Atributo nome do Árbitro
	 */
	private String nome;
	
/**
 * Construtor padrão da classe <b> Arbitro </b>
 */
	public Arbitro() {
		this.nome = nome;
	}

	
/**
 * Consultar nome do objeto Árbitro
 * @return String
 */
	public String getNome() {
		return nome;
	}
/**
 * Setar nome do Objeto Árbitro
 * @param nome
 */
	public void setNome(String nome) {
		this.nome = nome;
	}





	


}